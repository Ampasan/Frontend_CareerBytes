export const POPULAR_ROLE_NAMES = CAREER_ROLES.map((role) => role.name);

const normalizeText = (value = "") =>
  value.toString().toLowerCase().replace(/[^a-z0-9]/g, "");

export const getToolIconUrl = (toolName = "") => {
  if (TOOL_ICON_URLS[toolName]) return TOOL_ICON_URLS[toolName];

  const normalizedToolName = normalizeText(toolName);
  const match = Object.entries(TOOL_ICON_URLS).find(
    ([name]) => {
      const normalizedName = normalizeText(name);
      return (
        normalizedName === normalizedToolName ||
        normalizedName.includes(normalizedToolName) ||
        normalizedToolName.includes(normalizedName)
      );
    }
  );

  return match?.[1] || "";
};

export const getRoleConfig = (roleName = "") => {
  const normalizedRole = normalizeText(roleName);

  return CAREER_ROLES.find((role) => {
    const names = [role.name, ...(role.aliases || [])];
    return names.some((name) => {
      const normalizedName = normalizeText(name);
      return (
        normalizedName === normalizedRole ||
        normalizedName.includes(normalizedRole) ||
        normalizedRole.includes(normalizedName)
      );
    });
  });
};

export const getToolsForRoleLevel = (roleName = "", level = "") => {
  const role = getRoleConfig(roleName);
  const normalizedLevel = normalizeText(level).replace("advance", "advanced");

  return role?.tools?.[normalizedLevel] || [];
};
